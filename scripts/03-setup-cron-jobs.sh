"搜索并汇总过去24小时 AI 领域重要动态，重点关注：
1. 大模型（LLM）：新模型发布、benchmark、开源模型
2. AI Agent：新框架、新应用、行业落地
3. VLA（Vision-Language-Action）：具身智能、机器人相关
4. 端侧部署：模型压缩、量化、手机/边缘设备推理
信息源包括：arXiv、Hugging Face、GitHub trending、机器之心、量子位。
用中文输出，按重要程度排序，每条附原文链接。"

# ============================================
# 任务三：申博信息追踪（周一、四）
# ============================================
echo ""
echo "[3/14] 申博信息追踪 — 周一、四 08:00"

add_cron "申博追踪" "0 8 * * 1,4" \
"搜索国内 CS/AI 方向博士申请相关信息：
1. 各高校博士招生简章（尤其关注：西交、西电、西工大、清北、中科院、浙大、上交、南大、哈工大等 AI 强校）
2. 导师招生信息（AI/大模型/Agent/具身智能/VLA 方向）
3. 博士申请时间节点：报名、考核、面试时间
4. 申请经验分享（知乎、小红书、考博论坛）
信息源：各高校研究生院官网、导师个人主页、知乎、小红书。
按时间紧迫度排序推送。"

# ============================================
# 任务四：考公考编信息
# ============================================
echo ""
echo "[4/14] 考公考编信息 — 每天 08:30"

add_cron "考公考编" "30 8 * * *" \
"搜索西安及陕西省最新的考公考编信息，包括：
1. 国家公务员考试（国考）相关通知
2. 陕西省公务员考试（省考）报名、考试时间
3. 西安市事业单位招聘公告
4. 教师编制招聘（西安市、陕西省）
5. 选调生、三支一扶等
6. 军队文职招聘
信息源：陕西人事考试网、西安人社局、各区县政府网站、中公教育、华图教育。
重点标注：报名截止时间、考试时间、岗位要求。
按报名截止时间紧迫度排序。"

# ============================================
# 任务五：西安求职信息
# ============================================
echo ""
echo "[5/14] 西安求职监控 — 每天 09:00"

add_cron "求职监控" "0 9 * * *" \
"搜索适合 AI/CS 硕士的工作机会，筛选条件：
- 地点：西安优先，其次西安有办公室的大厂远程岗
- 方向：大模型、AI Agent、机器学习工程师、算法工程师、NLP、CV、机器人
- 来源：BOSS直聘、牛客、脉脉、各公司官网招聘页
- 关注企业：华为西安研究所、中兴、三星西安、荣耀、OPPO、字节西安、西安本地 AI 创业公司、研究院所（中科院西光所、西电相关等）
汇总新发布的岗位，包含：公司、岗位名、薪资范围、链接。
只推送新增岗位，不重复推送。"

