- Large Language Models / Foundation Models
- AI Agents / Tool Use
- Vision-Language-Action (VLA) / Embodied AI
- On-device / Edge Deployment / Model Compression
每个方向推荐 1-2 篇最值得读的，附标题、摘要概述和链接。"

# ============================================
# 任务九：校招专场监控（每周三）
# ============================================
echo ""
echo "[9/14] 校招专场监控 — 每周三 10:00"

add_cron "校招监控" "0 10 * * 3" \
"检查各大公司最新校招、秋招、春招、实习信息：
- 重点关注 AI/算法岗位
- 西安有岗位的优先
- 包括：网申截止时间、笔试面试时间
汇总推送。"

# ============================================
# 任务十：考编下周日历（每周日）
# ============================================
echo ""
echo "[10/14] 考编周历 — 每周日 19:00"

add_cron "考编周历" "0 19 * * 0" \
"汇总下周西安/陕西的考公考编重要时间节点：
- 哪些考试即将报名截止
- 哪些考试即将开考
- 新发布的招聘公告
