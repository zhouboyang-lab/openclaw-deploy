#!/bin/bash
# ============================================
# 第五阶段：配置定时任务
# 普通用户执行: bash scripts/03-setup-cron-jobs.sh
# ============================================

set -euo pipefail

TIMEZONE="Asia/Shanghai"

echo "=========================================="
echo " OpenClaw 定时任务配置"
echo " 时区: $TIMEZONE"
echo "=========================================="

# 检查 openclaw 命令是否可用
# CN-IM Docker 版需要通过 docker exec 执行
OPENCLAW_CONTAINER=$(docker ps --filter "name=openclaw" --format "{{.Names}}" | head -1)

if [ -z "$OPENCLAW_CONTAINER" ]; then
  echo "[错误] OpenClaw 容器未运行，请先执行 02-install-openclaw.sh"
  exit 1
fi

# 封装 openclaw 命令
openclaw() {
  docker exec "$OPENCLAW_CONTAINER" openclaw "$@"
}

echo ""
echo "OpenClaw 容器: $OPENCLAW_CONTAINER"
echo ""

# ---------- 辅助函数 ----------
add_cron() {
  local name="$1"
  local cron_expr="$2"
  local message="$3"
  local session="${4:-isolated}"

  echo "  添加: $name ($cron_expr)"

  if [ "$session" = "main" ]; then
    openclaw cron add \
      --name "$name" \
      --cron "$cron_expr" \
      --tz "$TIMEZONE" \
      --session main \
      --system-event "$message" \
      2>/dev/null && echo "    -> 成功" || echo "    -> 失败（可能已存在）"
  else
    openclaw cron add \
      --name "$name" \
      --cron "$cron_expr" \
      --tz "$TIMEZONE" \
      --session isolated \
      --message "$message" \
      2>/dev/null && echo "    -> 成功" || echo "    -> 失败（可能已存在）"
  fi
}

# ============================================
# 任务一：每日早安播报
# ============================================
echo ""
echo "[1/14] 早安播报 — 每天 07:00"

add_cron "早安播报" "0 7 * * *" \
"推送今日信息：
1. 西安今日天气和穿衣建议
2. 今天有什么重要日程/截止日期
3. 一句简短的励志/有趣的话
简洁明了，30 秒能看完。"

# ============================================
# 任务二：AI 领域日报
# ============================================
echo ""
echo "[2/14] AI 领域日报 — 每天 07:30"

