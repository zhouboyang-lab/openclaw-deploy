echo "[12/14] GitHub 热门 — 每天 21:00"

add_cron "GitHub热门" "0 21 * * *" \
"查看今日 GitHub Trending 中与 AI/ML 相关的项目：
- 新上榜的开源大模型、Agent 框架、推理工具
